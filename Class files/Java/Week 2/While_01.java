// The while loop

/*
 * A repetition control structure, which we refer as a loop, executes a block
 * of code multiple time in succession
 *
 * While loop -> a boolean expression (true/false) and a block of code
 */

public class While_01 {
  public static void main(String[] args) {
    int x = 5;

    while (x > 0) {
      System.out.println(x);
      x--;
    }
  }
}
