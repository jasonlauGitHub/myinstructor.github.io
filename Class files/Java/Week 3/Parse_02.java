// Parsing numbers

import java.util.*;

public class Parse_02 {
  public static void main(String[] args) {
    System.out.println(new Scanner("16").nextByte());
    System.out.println(new Scanner("42").nextInt());
    System.out.println(new Scanner("123456789").nextLong());
    System.out.println(new Scanner("5.3").nextFloat());
    System.out.println(new Scanner("67.43").nextDouble());
    System.out.println(new Scanner("true").nextBoolean());
  }
}
