// Operators

// ++, --
// *, /
// +, -

public class Operators {
  public static void main(String[] args) {
    int x = 9;
    int y = x + 1;

    int z = --x; // 8

    int w = 3 - x * 2;

    System.out.println("Y: " + y);
    System.out.println("Z: " + z);
    System.out.println("W: " + w);
  }
}
