// Java data types
// Primitive types (they are not objects)
// These data types can't use methods

public class Types {
  public static void main(String[] args) {
    boolean a = true;
    char b = 'a';
    byte c = 3;
    short d = 4;
    int e = 5;
    long f = 66666666l;
    float g = 4.0f;
    double h = 5.0;
  }
}
